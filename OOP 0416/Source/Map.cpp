	bool GameMap::IsEmpty(int x, int y) {
		int gx = x / MH;
		int gy = y / MW;
		return ((map[gy][gx] == 0) );
	}


	void GameMap::OnShow() {
		for (int i = 0; i < 120; i++)
		{
			for (int j = 0; j < 40; j++)
			{
				int sx = i * MW - X;
				int sy = j * MH - Y;

				switch (map[j][i])
				{
				case 0:
					break;
				case 1:
					block.SetTopLeft(sx,sy);
					block.ShowBitmap();
					break;
				case 2:
					floor.SetTopLeft(sx, sy);
					floor.ShowBitmap();
					break;
				case 3:
					floor_D.SetTopLeft(sx, sy);
					floor_D.ShowBitmap();
					break;
				default:
					ASSERT(0);
				}
			}

		}
	}
	int GameMap::ScreenX(int x) {
		return x - X;
	}
	int GameMap::ScreenY(int y) {
		return y - Y;
	}
	void GameMap::OnMove() {
		if (isMovingLeft)
			X -= 20;
		if (isMovingRight)
			X += 20;
		if (isMovingUp)
			Y -= 10;
		if (isMovingDown)
			Y += 20;
	}
